import {
  Links,
  Meta,
  Outlet,
  Scripts,
  ScrollRestoration,
} from "@remix-run/react";
import type { LinksFunction } from "@remix-run/node";

import { useMemo } from "react";

// import { Buffer } from "buffer";

// if (typeof window !== 'undefined') {
